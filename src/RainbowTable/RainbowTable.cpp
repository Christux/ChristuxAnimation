  RgbColor RainbowTable::getRainbowColor(uint16_t phase)
  {
    uint8_t red, green, blue;
    phase = phase < 360 ? phase : phase-360;

    // Accesses to flash memory instead of ram
    red=pgm_read_byte_near(rainbowTable+phase+120);
    green=pgm_read_byte_near(rainbowTable+phase);
    blue=pgm_read_byte_near(rainbowTable+phase+240);

    return RgbColor(red,green,blue);
  }

